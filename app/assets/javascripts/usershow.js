function Usershow2Bar1() {
  var yajirushi = document.getElementById("toukouyajirushi2");
  if ( yajirushi.className == "fas fa-chevron-down" ) {
    yajirushi.className = "fas fa-chevron-up";
  }else{
    yajirushi.className = "fas fa-chevron-down";
  }
};

